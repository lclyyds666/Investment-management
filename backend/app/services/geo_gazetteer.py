# 预排序：按城市名长度倒序，保证「西双版纳」「张家界」等长名先于短名匹配。
_CITY_KEYS_BY_LEN = sorted(CITY_COORDS.keys(), key=len, reverse=True)
# 省名去掉后缀的检索表（如 "山东" → "山东省"），支持项目名只写省份简称。
_PROVINCE_ALIASES: dict[str, str] = {}
for _full in PROVINCE_COORDS:
    _core = (
        _full.replace("省", "").replace("市", "")
        .replace("特别行政区", "").replace("自治区", "")
        .replace("壮族", "").replace("回族", "").replace("维吾尔", "")
    )
    if _core:
        _PROVINCE_ALIASES[_core] = _full
_PROVINCE_KEYS_BY_LEN = sorted(_PROVINCE_ALIASES.keys(), key=len, reverse=True)


def resolve_city(text: str) -> tuple[str, str, float, float] | None:
    """从任意文本（通常是项目名）解析城市 → (城市, 省, lng, lat)。

    1. 先在城市词典里找最长命中（"泉州欧乐堡" → 泉州）。
    2. 找不到城市则退而求其次匹配省名（"山东文旅项目" → 山东省，落省中心）。
    3. 都匹配不到返回 None。
    """
    if not text:
        return None
    s = str(text).strip()
    for city in _CITY_KEYS_BY_LEN:
        if city in s:
            prov, lng, lat = CITY_COORDS[city]
            return city, prov, lng, lat
    for alias in _PROVINCE_KEYS_BY_LEN:
        if alias in s:
            full = _PROVINCE_ALIASES[alias]
            lng, lat = PROVINCE_COORDS[full]
            return full, full, lng, lat
    return None
