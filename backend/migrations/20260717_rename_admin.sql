--  管理员账号显示名更名（2026-07-17）
--  「系统管理员」→「信息维护」
--  说明：仅更新 admin 账号且当前显示名仍为旧值时命中，幂等可重复执行；
--       角色值(role)不变，不影响任何权限与历史数据。
