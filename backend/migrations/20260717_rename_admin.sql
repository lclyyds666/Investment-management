USE `sd_publish_scm`;

UPDATE `sys_user`
   SET `full_name` = '信息维护'
 WHERE `username` = 'admin'
   AND `full_name` = '系统管理员';

SELECT '管理员显示名已更名为「信息维护」（如已更名则命中 0 行）。' AS message;
